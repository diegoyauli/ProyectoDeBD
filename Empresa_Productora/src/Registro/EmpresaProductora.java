package Registro;

public class EmpresaProductora {
    private int empCod;
    private String empNom;
    private String empRUC;
    private String empDir;
    private String empAct;
    private int regCod;
    private String estReg;

    public int getEmpCod() {
        return empCod;
    }

    public void setEmpCod(int empCod) {
        this.empCod = empCod;
    }

    public String getEmpNom() {
        return empNom;
    }

    public void setEmpNom(String empNom) {
        this.empNom = empNom;
    }

    public String getEmpRUC() {
        return empRUC;
    }

    public void setEmpRUC(String empRUC) {
        this.empRUC = empRUC;
    }

    public String getEmpDir() {
        return empDir;
    }

    public void setEmpDir(String empDir) {
        this.empDir = empDir;
    }

    public String getEmpAct() {
        return empAct;
    }

    public void setEmpAct(String empAct) {
        this.empAct = empAct;
    }

    public int getRegCod() {
        return regCod;
    }

    public void setRegCod(int regCod) {
        this.regCod = regCod;
    }

    public String getEstReg() {
        return estReg;
    }

    public void setEstReg(String estReg) {
        this.estReg = estReg;
    }
}
