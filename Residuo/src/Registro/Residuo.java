package Registro;

public class Residuo {
    private int resCod;
    private String resNom;
    private String resDes;
    private String codResCod;
    private int empCod;

    public int getResCod() {
        return resCod;
    }

    public void setResCod(int resCod) {
        this.resCod = resCod;
    }

    public String getResNom() {
        return resNom;
    }

    public void setResNom(String resNom) {
        this.resNom = resNom;
    }

    public String getResDes() {
        return resDes;
    }

    public void setResDes(String resDes) {
        this.resDes = resDes;
    }

    public String getCodResCod() {
        return codResCod;
    }

    public void setCodResCod(String codResCod) {
        this.codResCod = codResCod;
    }

    public int getEmpCod() {
        return empCod;
    }

    public void setEmpCod(int empCod) {
        this.empCod = empCod;
    }
}
