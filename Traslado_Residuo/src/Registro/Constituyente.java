package Registro;

public class Constituyente {
    private int consCod;
    private String conNom;
    private String conDes;
    private Byte conGrad;

    public int getConsCod() {
        return consCod;
    }

    public void setConsCod(int consCod) {
        this.consCod = consCod;
    }

    public String getConNom() {
        return conNom;
    }

    public void setConNom(String conNom) {
        this.conNom = conNom;
    }

    public String getConDes() {
        return conDes;
    }

    public void setConDes(String conDes) {
        this.conDes = conDes;
    }

    public Byte getConGrad() {
        return conGrad;
    }

    public void setConGrad(Byte conGrad) {
        this.conGrad = conGrad;
    }
}
