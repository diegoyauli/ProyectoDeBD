
package Registro;

public class Residuos {
    private int ResCod;
    private String ResNom;
    private String ResDes;
    private String ResEstReg;

    // Getters y Setters
    public int getResCod() {
        return ResCod;
    }

    public void setResCod(int ResCod) {
        this.ResCod = ResCod;
    }

    public String getResNom() {
        return ResNom;
    }

    public void setResNom(String ResNom) {
        this.ResNom = ResNom;
    }

    public String getResDes() {
        return ResDes;
    }

    public void setResDes(String ResDes) {
        this.ResDes = ResDes;
    }

    public String getResEstReg() {
        return ResEstReg;
    }

    public void setResEstReg(String ResEstReg) {
        this.ResEstReg = ResEstReg;
    }
}


